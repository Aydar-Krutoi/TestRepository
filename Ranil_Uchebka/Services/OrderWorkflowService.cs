namespace Ranil_Uchebka.Services;

public static class OrderWorkflowService
{
    public static readonly string[] AllStatuses =
    [
        "Новый",
        "Отклонен",
        "Составление спецификации",
        "Подтверждение",
        "Закупка",
        "Производство",
        "Контроль",
        "Готов",
        "Закрыт"
    ];

    private static readonly Dictionary<string, string?> NextStatus = new()
    {
        ["Новый"] = "Составление спецификации",
        ["Составление спецификации"] = "Подтверждение",
        ["Подтверждение"] = "Закупка",
        ["Закупка"] = "Производство",
        ["Производство"] = "Контроль",
        ["Контроль"] = "Готов",
        ["Готов"] = "Закрыт"
    };

    public static bool CanTransition(string from, string to) =>
        to == "Отклонен" || (NextStatus.TryGetValue(from, out var next) && next == to);

    /// <summary>По WSR2018_DE09-C2: кто может перевести заказ в целевой статус.</summary>
    public static bool CanRoleChangeStatus(string role, string from, string to)
    {
        if (!CanTransition(from, to))
        {
            return false;
        }

        return role switch
        {
            "Менеджер" => (from, to) switch
            {
                ("Новый", "Составление спецификации") => true,
                ("Новый", "Отклонен") => true,
                ("Подтверждение", "Закупка") => true,
                ("Подтверждение", "Отклонен") => true,
                ("Закупка", "Производство") => true,
                ("Готов", "Закрыт") => true,
                _ => false
            },
            "Конструктор" => from == "Составление спецификации" && to == "Подтверждение",
            "Мастер" => (from, to) switch
            {
                ("Производство", "Контроль") => true,
                ("Контроль", "Готов") => true,
                _ => false
            },
            "Заказчик" => to == "Отклонен" && from is "Новый" or "Составление спецификации" or "Подтверждение",
            "Директор" => false,
            _ => false
        };
    }

    public static string FilterNew => "new";
    public static string FilterCurrent => "current";
    public static string FilterDone => "done";
    public static string FilterRejected => "rejected";

    public static IReadOnlyList<string> GetAllowedTargetStatuses(string role, string from)
    {
        var result = new List<string>();
        foreach (var to in AllStatuses)
        {
            if (to != from && CanRoleChangeStatus(role, from, to))
            {
                result.Add(to);
            }
        }

        return result;
    }

    public static string GetTransitionDisplayName(string from, string to) => (from, to) switch
    {
        ("Новый", "Составление спецификации") => "Принять заказ",
        ("Новый", "Отклонен") => "Отклонить заявку",
        ("Составление спецификации", "Подтверждение") => "Спецификация готова → Подтверждение",
        ("Подтверждение", "Закупка") => "Клиент согласен → Закупка",
        ("Подтверждение", "Отклонен") => "Клиент отказался → Отклонен",
        ("Закупка", "Производство") => "Материалы получены → Производство",
        ("Производство", "Контроль") => "Производство завершено → Контроль",
        ("Контроль", "Готов") => "Качество ОК → Готов",
        ("Готов", "Закрыт") => "Отгружен и оплачен → Закрыт",
        _ => $"→ {to}"
    };

    public static string GetStatusHelpForRole(string role, string currentStatus) => (role, currentStatus) switch
    {
        ("Менеджер", "Составление спецификации") =>
            "На этом этапе статус меняет конструктор. Войдите как конструктор, укажите стоимость и срок, затем выберите «Спецификация готова → Подтверждение».",
        ("Менеджер", "Производство" or "Контроль") =>
            "На этом этапе статус меняет мастер производства.",
        ("Конструктор", "Новый" or "Подтверждение" or "Закупка" or "Производство" or "Контроль" or "Готов" or "Закрыт" or "Отклонен") =>
            "Для вашей роли на этом этапе смена статуса недоступна.",
        ("Мастер", "Новый" or "Составление спецификации" or "Подтверждение" or "Закупка" or "Готов" or "Закрыт" or "Отклонен") =>
            "Для вашей роли на этом этапе смена статуса недоступна.",
        ("Мастер", "Производство") =>
            "Выберите в списке «Производство завершено → Контроль» и нажмите «Сохранить статус».",
        ("Мастер", "Контроль") =>
            "Сначала проверьте качество в разделе «Качество», затем выберите «Качество ОК → Готов».",
        ("Менеджер", "Новый") =>
            "Выберите «Принять заказ» или «Отклонить заявку», затем нажмите «Сохранить статус».",
        ("Заказчик", "Новый" or "Составление спецификации" or "Подтверждение") =>
            "Отмена доступна до этапа «Закупка». Выберите «Отменить заказ» и нажмите «Сохранить статус».",
        _ => "Выберите новый статус из списка и нажмите «Сохранить статус»."
    };

    public static IReadOnlyList<string> StatusesForFilter(string filter) => filter switch
    {
        "new" => ["Новый", "Составление спецификации", "Подтверждение"],
        "current" => ["Закупка", "Производство", "Контроль"],
        "done" => ["Готов", "Закрыт"],
        "rejected" => ["Отклонен"],
        _ => AllStatuses
    };
}
